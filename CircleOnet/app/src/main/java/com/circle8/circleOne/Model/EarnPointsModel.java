public class EarnPointsModel
{
    String earnId ;
    String pointEarned ;
    String benefitName ;
    String benefitDesc ;
    String benefitDate ;

    public String getEarnId() {
        return earnId;
    }

    public void setEarnId(String earnId) {
        this.earnId = earnId;
    }

    public String getPointEarned() {
        return pointEarned;
    }

    public void setPointEarned(String pointEarned) {
        this.pointEarned = pointEarned;
    }

    public String getBenefitName() {
        return benefitName;
    }

    public void setBenefitName(String benefitName) {
        this.benefitName = benefitName;
    }

    public String getBenefitDesc() {
        return benefitDesc;
    }

    public void setBenefitDesc(String benefitDesc) {
        this.benefitDesc = benefitDesc;
    }

    public String getBenefitDate() {
        return benefitDate;
    }

    public void setBenefitDate(String benefitDate) {
        this.benefitDate = benefitDate;
    }
}
