public class TestimonialModel {

    String Testimonial_ID = "" ;
    String CompanyName = "";
    String Designation = "";
    String FirstName = "";
    String FriendProfileID = "";
    String LastName = "";
    String Purpose = "";
    String Status = "";
    String Testimonial_Text = "";
    String UserPhoto = "";

    public TestimonialModel() {
    }

    public String getTestimonial_ID() {
        return Testimonial_ID;
    }

    public void setTestimonial_ID(String testimonial_ID) {
        Testimonial_ID = testimonial_ID;
    }

    public String getCompanyName() {
        return CompanyName;
    }

    public void setCompanyName(String companyName) {
        CompanyName = companyName;
    }

    public String getDesignation() {
        return Designation;
    }

    public void setDesignation(String designation) {
        Designation = designation;
    }

    public String getFirstName() {
        return FirstName;
    }

    public void setFirstName(String firstName) {
        FirstName = firstName;
    }

    public String getFriendProfileID() {
        return FriendProfileID;
    }

    public void setFriendProfileID(String friendProfileID) {
        FriendProfileID = friendProfileID;
    }

    public String getLastName() {
        return LastName;
    }

    public void setLastName(String lastName) {
        LastName = lastName;
    }

    public String getPurpose() {
        return Purpose;
    }

    public void setPurpose(String purpose) {
        Purpose = purpose;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }

    public String getTestimonial_Text() {
        return Testimonial_Text;
    }

    public void setTestimonial_Text(String testimonial_Text) {
        Testimonial_Text = testimonial_Text;
    }

    public String getUserPhoto() {
        return UserPhoto;
    }

    public void setUserPhoto(String userPhoto) {
        UserPhoto = userPhoto;
    }
}
