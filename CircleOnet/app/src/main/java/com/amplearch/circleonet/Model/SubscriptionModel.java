public class SubscriptionModel
{
    String package_no ;
    String contact ;
    String groups ;
    String connect ;
    String money ;
    String bill ;
    String letf_connection ;

    public String getPackage_no() {
        return package_no;
    }

    public void setPackage_no(String package_no) {
        this.package_no = package_no;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getGroups() {
        return groups;
    }

    public void setGroups(String groups) {
        this.groups = groups;
    }

    public String getConnect() {
        return connect;
    }

    public void setConnect(String connect) {
        this.connect = connect;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getBill() {
        return bill;
    }

    public void setBill(String bill) {
        this.bill = bill;
    }

    public String getLetf_connection() {
        return letf_connection;
    }

    public void setLetf_connection(String letf_connection) {
        this.letf_connection = letf_connection;
    }
}
